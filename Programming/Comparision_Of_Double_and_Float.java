package Programming;

//Size of mantissa for float is 24 and 53 for double.
public class Comparision_Of_Double_and_Float {

	public static void main(String[] args) {
		float f = 5.25f;
	    double d = 5.25;
	    System.out.println(f == d);  //true
	    float f1 = 5.1f;
	    double d1 = 5.1;
	    System.out.println(f1 == d1);  //false

	}

}
